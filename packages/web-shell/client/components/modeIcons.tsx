const MODE_ICONS: Record<string, (props: IconProps) => ReactNode> = {
  plan: PlanIcon,
  default: HandIcon,
  'auto-edit': PencilIcon,
  auto: ZapIcon,
  yolo: ShieldAlertIcon,
};

export function ModeIcon({ mode, ...props }: IconProps & { mode: string }) {
  const Icon = MODE_ICONS[mode] ?? HandIcon;
  return <>{Icon(props)}</>;
}

