/*
 * Stroke icons for the approval modes and the model chip, drawn feather-style
 * (stroke = currentColor) like the StatusBar gear so they inherit the chip's
 * color. Shared by the StatusBar config chips and the ApprovalModeMessage menu
 * so a mode reads the same in both places.
 */
import type { ReactNode } from 'react';

interface IconProps {
  className?: string;
  size?: number;
}

function Svg({
  className,
  size = 14,
  children,
}: IconProps & { children: ReactNode }) {
  return (
    <svg
      className={className}
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
    >
      {children}
    </svg>
  );
}

